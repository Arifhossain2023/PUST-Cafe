import mongoose from 'mongoose';

const kitchenSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const Kitchen = mongoose.model('Kitchen', kitchenSchema);
export default Kitchen;
